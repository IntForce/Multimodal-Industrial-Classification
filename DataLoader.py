    def _metricConverter(self, metric : str) -> float:
        metric = str(metric).strip().lower()
        
        # booleans
        if metric == "true":
            return 1.0
        if metric == "false":
            return 0.0
        
        # empty metrics
        if metric == "":
            return 0.0
        
        # numbers
        try:
            return float(metric)

        except ValueError:
            print(f"[DataLoader] > Warning: Could not convert metric to float when loading data.")
            try:
                return float(metric.replace(",", "."))
            except ValueError:
                print(f"[DataLoader] > Warning: ValueError occured when converting metric to float. Returning 0.0 instead.")
                return 0.0
    
