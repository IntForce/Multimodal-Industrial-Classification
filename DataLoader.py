    def classifySolidLettersLib(self):
        """
        This function establishes the ground truth e.g. labels for the SolidLetters library.
        It iterates through all loaded edge contours, and extracts the labels from the filenames prefix. The function handles upper and lower case filenames,
        such that both types will be labeled as a class e.g. the letter `a` will have classes `a` and `A`.
        
        Args:
            None.
        Returns:
            None.
        """
        print("[DataLoader > Initializing the classification of the SolidLetters library.]")
        for entry in self._data.values():
            className = str(entry.get("name", "")).strip()
            className = os.path.splitext(className)[0]
            
            # Extract just the first letter
            first_char = className[:1]
            
            # Check if name contains "upper" or "lower" (using .lower() for a case-insensitive check)
            if "upper" in className.lower():
                entry["class"] = first_char.upper()
            elif "lower" in className.lower():
                entry["class"] = first_char.lower()
            else:
                # Fallback just in case neither is in the name
                entry["class"] = first_char
                
            print(f"[DataLoader] > Ground truth established for data entry {entry.get('name')} as {entry['class']}.")
        print("[DataLoader] > The contours from SolidLetters has been successfully classified - class ground truth is now available.")
    
    def classifyTracePartsLib(self, csvPath):
        """
        This function establishes the ground truth e.g. labels for the TraceParts library using a metadata .csv file.
        This is done by making a lookup table from the provided .csv file and iterating through it to extract the given filenames and classs.
        Each filename and class is saved to the lookup table while invalid data entries are ignored.

        Args:
            csvPath (str): Absolute file path for the TraceParts metadata .csv file (e.g. `categories.csv`).
        Returns:
            None.
        Raises:
            FileNotFoundError: If the provided input argument is invalid or the .csv filepath does not exist.
        """
        if csvPath is None:
            print("[DataLoader] > input parameter 'csvPath' cannot be of type None. Function requires metadata csv file to exist.")
            return
        
        tempDict: dict = {}
        
        with open(csvPath, "r", encoding="utf-8") as file:
            csvReader = csv.reader(file, delimiter=';')
            
            next(csvReader)
            
            for row in csvReader:
                if len(row) == 1 and "\t" in row[0]:
                    row = row[0].split("\t")
                
                if len(row) < 5:
                    continue
                
                partName = row[0].split(".")[0].lower().strip()
                partCategory = row[4].lower().strip()
                
                # if partName in self._data.keys():
                tempDict[partName] = partCategory
        
        for entry in self._data:
            bname = entry.split(".")[0].lower().strip()
            if bname in tempDict:
                category = tempDict.get(bname)
                if category != "" and category != "uncategorized":
                    self._data[entry]["class"] = category
                else:
                    self._data[entry]["class"] = ""
            else:
                self._data[entry]["class"] = ""
    
