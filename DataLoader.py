class DataWrapper(Dataset):
    """
    This class only exists to integrate the custom data loader module into Pytorch.
    It takes the numpy arrays given the train, validation and test data and loads it into Pytorch tensors while assigning class labels of type `int` to the respective data points.
    """
    def __init__(self, dataLoader: DataLoader, split="train"): #: DataLoader <<<<<<<<<-------
        """
        Initializes pytorch dataset splits.

        Args:
            dataLoader (DataLoader): The custom dataloader module containing the data to be processed.
            split (str, optional): Specifies what split to retrieve (e.g. train, validation, or test). Defaults to "train".

        Raises:
            ValueError: If the provided split string is invalid.
        """
        self._loader = dataLoader
        classes = self._loader.getClassNames()
        self._classDict: dict = {className: i for i, className in enumerate(classes)}
        
        if split == "train":
            self._data = self._loader._train
        elif split == "validation":
            self._data = self._loader._validation
        elif split == "test":
            self._data = self._loader._test
        else:
            raise ValueError("The split fetched from the custom data loader 'dataLoader' must be specified as either: 'train', 'validation', or 'test'.")
    
    def __len__(self):
        """
        Returns the total number of samples in the specified dataset split. This function must be implemented in the datawrapper:
        https://docs.pytorch.org/tutorials/beginner/basics/data_tutorial.html#creating-a-custom-dataset-for-your-files

        Returns:
            int: The length of the data split.
        """
        return len(self._data)
    
    def __getitem__(self, index):
        """
        Get a single item sample from the dataset. The function extracts the normalized edge contours and the normalized metrics, converts
        them into respective pytorch tensors such that they can be processed. The class ground truths are also mapped to the respective samples in each tensor.

        Args:
            index (int): Specifies the index of what item sample to retrieve.

        Returns:
            tuple: A tupple of size 3 containing:
                - edgeTensor (torch.Tensor): The tensor containing visual data.
                - metricTensor (torch.Tensor): The tensor containing tabular data.
                - labelTensor (torch.tensor): Class ground truths.
        """
        img = self._data[index]
        
        edge = self._loader.getEdge(img)
        metric = self._loader.getMetric(img)
        classLabel = self._loader._data[img]["class"] # get ground truth class for image
        
        label = self._classDict[classLabel]
        
        edgeTensor = torch.tensor(edge, dtype=torch.float32)
        metricTensor = torch.tensor(metric, dtype=torch.float32)
        labelTensor = torch.tensor(label, dtype=torch.long)
        
        return edgeTensor, metricTensor, labelTensor
        