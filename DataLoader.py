    def getNumOfClasses(self):
        """
        Returns the number of classes in the data.

        Returns:
            int: The number of total classes regardless of data split.
        """
        classes = set()
        
        for entry in self._data.values():
            category = entry.get("class", "").strip()
            
            if category != "":
                classes.add(category)
        
        return len(classes)
    
    def getClassNames(self):
        """
        Get the unique class names.

        Returns:
            list: A sorted list of all unique class names in the data.
        """
        classes = set()
        
        for entry in self._data.values():
            category = entry.get("class", "").strip()

            if category != "":
                classes.add(category)
        
        return sorted(list(classes), key=lambda c: (c.lower(), not c.islower()))
    
