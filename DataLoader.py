    def _saveMatch(self, imageName: str, path : str, metrics : list, category : str = ""):
        if path == "" or metrics == []:
            print(f"[DataLoader] > Error: Could not save match. Either the path is not specified or the metrics vector is not parsed. Skipping.")
            return
        
        if category == "Uncategorized":
            category = ""
        
        self._data[imageName] = {"name": imageName, "edge_path": path, "metrics": metrics, "class": category}
        print(f"[DataLoader] > Saved entry: ({imageName}:{category}), with path: {path}")
    
    def splitData(self, trainRatio : float, validationRatio : float, testRatio : float):
        """
        Splits the data into stratisfied train, validation, and test with the given ratios. This implies each data portion contains a class sample.

        Args:
            trainRatio (float): Ratio of training data.
            validationRatio (float): Ratio of validation data.
            testRatio (float): Ratio of testing data.
        Returns:
            None.
        """
        # if another library is used it would be smart to create a new helper func to assert the ground truth for that library such as the one used below
        # self.classifySolidLettersLib() # ground truth for classes (for solidLetters lib)
        
        allParts = []
        allLabels = []
        
        for key, entry in self._data.items():
            category = str(entry.get("class", "")).strip()
            
            # skip uncateorizaziable entries
            if category == "":
                continue
            
            allParts.append(entry.get("name", ""))
            allLabels.append(entry.get("class"))

        print(f"[DataLoader] > Total valid samples found for training: {len(allParts)}")
        # #indexes and assignmetn
        # trainIndex = int(len(allParts) * trainRatio)
        # validationIndex = int(len(allParts) * validationRatio) + trainIndex
        # testIndex = int(len(allParts) * testRatio) + validationIndex
        
        # random.shuffle(allParts)
        
        # self._train = allParts[:trainIndex]
        # self._validation = allParts[trainIndex:validationIndex]
        # self._test = allParts[validationIndex:]

        classCounter = Counter(allLabels)
        minSampleAmount = 7 # min amount of samples allowed to be in a given class. parametr is for stratisfying
        
        filteredParts = []
        filteredLabels = []

        for part, label in zip(allParts, allLabels):
            if classCounter[label] >= minSampleAmount:
                filteredParts.append(part)
                filteredLabels.append(label)
        
        allParts = filteredParts
        allLabels = filteredLabels
        
        if len(allParts)  == 0:
            print(f"[DataLoader] > After filtering the parts in order to stratisfy the data no samples is left. More data required.")
        
        # outfilter datapunkter der ikke kan stratifiseres
        self._data = {part: self._data[part] for part in allParts}
        
        self._train, secondSplitParts, _, secondSplitLabels = train_test_split(allParts, allLabels, train_size=trainRatio, test_size=validationRatio+testRatio, stratify=allLabels, random_state=0)

        testSize = testRatio / (validationRatio + testRatio)
        
        self._validation, self._test, _, _ = train_test_split(secondSplitParts, secondSplitLabels, test_size=testSize, stratify=secondSplitLabels, random_state=0)
        
        # mean of the metrics (determining z score variable: meanOfMetrics to normalize metrics later on)
        metricsMean = np.array([self._data[metric]["metrics"] for metric in self._train], dtype=np.float32)
        self._meanOfTrainedMetrics = np.mean(metricsMean, axis=0)
        self._stdOfTrainedMetrics = np.std(metricsMean, axis=0) + 1e-16
        
        print(f"[DataLoader] > Data has been split into train, validation, and test sets successfully.")
    
    # getters
    def getEdge(self, name : str) -> np.ndarray:
        """
        Returns the edge contour given its filename. This function normalizes the image to have pixel intensity between 0 and 1

        Args:
            name (str): Name of the desired edge contour.

        Returns:
            np.ndarray: The specified contour.
        """
        path = self._data[name]["edge_path"]
        contour = cv2.imread(path).astype(np.float32)
        contour = contour / 255.0 # https://www.geeksforgeeks.org/computer-vision/normalize-an-image-in-opencv-python/
        contour = np.transpose(contour, (2, 0, 1)) # https://docs.pytorch.org/tutorials/intermediate/memory_format_tutorial.html
        return contour
    
    def getMetric(self, name : str) -> np.ndarray:
        """
        Returns the tabular data metric given the specified filename. This function normalizes the tabular data via z-score normalization using the mean and standard deviation
        calculated using the training data preventing data leakage.

        Args:
            name (str): Name of the file for which the tabular metric should be returned.

        Returns:
            np.ndarray: Tabular metric.
        """
        metrics = self._data[name]["metrics"]
        metrics = np.array(metrics, dtype=np.float32)
        metrics = (metrics - self._meanOfTrainedMetrics) / self._stdOfTrainedMetrics # formular: Z = (X - mu)/sigma --- https://www.geeksforgeeks.org/machine-learning/feature-engineering-scaling-normalization-and-standardization/
        return metrics

    def getData(self) -> dict:
        """
        Get the internal class dictionary containing following structure:
        {"name": imageName, "edge_path": path, "metrics": metrics, "class": category}.

        Returns:
            dict: data dictionary.
        """
